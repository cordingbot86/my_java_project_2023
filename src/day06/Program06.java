package day06;

public class Program06 {

	public static void main(String[] args) {
		//+ - * / (Q) %(R)  Number
		int a=45;
		int b=5;
		double c=5;
		//int () int--->int
		//int () double/float---->double/float
		
		System.out.println(a+b);
		System.out.println(a+c);
		
		
		System.out.println(a-b);
		System.out.println(a-c);
		
		
		System.out.println(a*b);
		System.out.println(a*c);
		
		System.out.println(a/b);
		System.out.println(a/c);
		
		System.out.println(a%b);
		System.out.println(a%c);
		
		
	}

}
